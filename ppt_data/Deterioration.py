# ─────────────────────────────────────────────────────────
# 경기도 주거용 건축물: 전체 vs 30년 이상 노후건물 막대그래프
# 데이터: deterioration.xlsx (건축물 생애이력 관리 시스템)
# ─────────────────────────────────────────────────────────

import pandas as pd               # 엑셀/표 데이터를 다루는 라이브러리
import matplotlib.pyplot as plt   # 그래프 그리는 라이브러리 (관례상 plt로 줄여 씀)

# ══════════════════════════════════════════════
# 1단계: 엑셀 파일 읽기
# ══════════════════════════════════════════════
df = pd.read_excel(
    'deterioration.xlsx',   # 읽을 파일 (파이썬 파일과 같은 폴더에 있어야 함)
    skiprows=3,              # 엑셀 맨 위 3줄은 제목/헤더라서 건너뜀
    header=None,             # "첫 줄을 컬럼명으로 쓰지 마" 라는 뜻
    names=[                  # 컬럼명을 우리가 직접 지정
        '시도','시군구','읍면동','경과년수','합계',   # 실제로 쓸 5개
        'c1','c2','c3','c4','c5','c6',              # 뒤쪽 주택유형별 컬럼들
        'c7','c8','c9','c10','c11'                  # (이번엔 안 쓰지만 자리는 맞춰야 함)
    ]
)

# 16개 컬럼 중 그래프에 필요한 5개만 남김
df = df[['시도','시군구','읍면동','경과년수','합계']]

# ══════════════════════════════════════════════
# 2단계: 총계 행 제거
# ──────────────────────────────────────────────
# 데이터 첫 행에 시도가 NaN(빈칸)이고 합계만 658079인
# "경기도 전체 합계" 행이 숨어 있음.
# 이걸 안 지우면 3단계 sum()에서 전체가 정확히 2배로 나옴!
# ══════════════════════════════════════════════
df = df.dropna(subset=['시도'])   # '시도' 컬럼이 빈칸인 행을 삭제

# ══════════════════════════════════════════════
# 3단계: 조건별 집계
# ══════════════════════════════════════════════
total = df['합계'].sum()   # 모든 행의 '합계'를 더함 = 경기도 전체 건물 수

# df.loc[조건, '컬럼'] = "조건에 맞는 행에서 그 컬럼만 뽑기"
sel_30 = df.loc[df['경과년수'] == '30~35년 미만', '합계'].sum()  # 30~35년 구간 합
sel_35 = df.loc[df['경과년수'] == '35년 이상',   '합계'].sum()  # 35년 이상 구간 합

sel_old = sel_30 + sel_35   # 두 구간을 합쳐 "30년 이상 노후건물"로 통합

# f-string의 {변수:,} 는 천 단위 콤마 표시 (658079 → 658,079)
print(f'전체: {total:,} / 30년 이상: {sel_old:,}')
# 정상이면 → 전체: 658,079 / 30년 이상: 187,802

# ══════════════════════════════════════════════
# 4단계: 막대그래프 그리기
# ══════════════════════════════════════════════

# 한글 폰트 설정 (이거 없으면 그래프의 한글이 전부 □□□로 깨짐)
plt.rcParams['font.family'] = 'Malgun Gothic'    # 윈도우 기본 한글 폰트
plt.rcParams['axes.unicode_minus'] = False       # 마이너스(-) 기호 깨짐 방지

labels = ['전체', '30년 이상 노후건물']   # x축에 표시될 이름 (막대 순서대로)
values = [total, sel_old]                # 각 막대의 높이 (labels와 순서 일치!)

# figure(도화지)와 axes(그래프 영역) 생성. figsize=(가로, 세로) 인치 단위
fig, ax = plt.subplots(figsize=(7, 5))

# 막대그래프 생성. color는 막대 색 (회색=전체, 빨강=노후 → 위험 강조)
bars = ax.bar(labels, values, color=['#9aa5b1', '#d9534f'])

ax.set_title('경기도 주거용 건축물: 전체 대비 30년 이상 노후건물')  # 그래프 제목
ax.set_ylabel('건축물 수 (동)')                                      # y축 이름

# 각 막대 위에 숫자 표시 (PPT에서 눈금 안 읽어도 값이 바로 보이게)
for bar in bars:
    h = bar.get_height()                # 막대의 높이 = 그 막대의 값
    ax.text(
        bar.get_x() + bar.get_width()/2,  # x좌표: 막대의 가로 중앙
        h,                                 # y좌표: 막대 꼭대기
        f'{h:,.0f}',                       # 표시할 글자 (콤마 포함, 소수점 없이)
        ha='center',                       # 가로 정렬: 중앙
        va='bottom'                        # 세로 정렬: 막대 위에 붙게
    )

plt.tight_layout()                              # 여백 자동 정리 (글자 잘림 방지)
plt.savefig('deterioration_bar.png', dpi=150)   # PPT용 이미지 저장 (dpi=해상도)
plt.show()                                      # 화면에 그래프 창 띄우기